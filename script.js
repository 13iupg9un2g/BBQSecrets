const partsContainer = document.getElementById('parts-container');
const animalsSection = document.getElementById('animals-section');
const marinadesSection = document.getElementById('marinades-section');
const marinadesContainer = document.getElementById('marinades-container');
const recipeSection = document.getElementById('recipe-section');
const recipeContent = document.getElementById('recipe-content');

let selectedAnimal = null;
let selectedPart = null;

function createCard(name, className = 'meat-card') {
  const div = document.createElement('div');
  div.className = className;
  div.textContent = name;
  return div;
}

// Загрузка животных
function loadAnimals() {
  animalsContainer.innerHTML = '';
  for (const animal in data) {
    const card = createCard(capitalize(animal));
    card.addEventListener('click', () => {
      selectedAnimal = animal;
      showParts(animal);
    });
    animalsContainer.appendChild(card);
  }
}

// Показать части выбранного животного
function showParts(animal) {
  partsContainer.innerHTML = '';
  data[animal].forEach(part => {
    const card = createCard(part, 'part-card');
    card.addEventListener('click', () => {
      selectedPart = part;
      showMarinades();
    });
    partsContainer.appendChild(card);
  });
  animalsSection.classList.add('hidden');
  partsSection.classList.remove('hidden');
  marinadesSection.classList.add('hidden');
  recipeSection.classList.add('hidden');
}

// Показать выбор маринада
function showMarinades() {
  animalsSection.classList.add('hidden');
  partsSection.classList.add('hidden');
  marinadesSection.classList.remove('hidden');
  recipeSection.classList.add('hidden');
}

// Показать рецепт выбранного маринада
function showRecipe(type) {
  const recipe = recipesData[selectedAnimal]?.[selectedPart]?.[type];
  if (recipe) {
    recipeContent.innerHTML = recipe;
  } else {
    recipeContent.innerHTML = '<p>Рецепт не найден.</p>';
  }

  animalsSection.classList.add('hidden');
  partsSection.classList.add('hidden');
  marinadesSection.classList.add('hidden');
  recipeSection.classList.remove('hidden');
}

// Кнопки назад
document.getElementById('back-to-animals').addEventListener('click', () => {
  partsSection.classList.add('hidden');
  animalsSection.classList.remove('hidden');
  marinadesSection.classList.add('hidden');
  recipeSection.classList.add('hidden');
});

document.getElementById('back-to-parts').addEventListener('click', () => {
  marinadesSection.classList.add('hidden');
  partsSection.classList.remove('hidden');
  animalsSection.classList.add('hidden');
  recipeSection.classList.add('hidden');
});

document.getElementById('back-to-marinades').addEventListener('click', () => {
  recipeSection.classList.add('hidden');
  marinadesSection.classList.remove('hidden');
  partsSection.classList.add('hidden');
  animalsSection.classList.add('hidden');
});

// Маринады выбор
marinadesContainer.querySelectorAll('button').forEach(button => {
  button.addEventListener('click', () => {
    showRecipe(button.dataset.type);
  });
});

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

